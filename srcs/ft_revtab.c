#include "libft.h"

void	ft_revtab(void **tab, int len)
{
	int		i;
	void	*tmp;

	i = -1;
	while (++i < len / 2)
	{
		tmp = tab[i];
		tab[i] = tab[len - i - 1];
		tab[len - i - 1] = tmp;
	}
}
