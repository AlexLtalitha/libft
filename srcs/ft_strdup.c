#include "libft.h"

char	*ft_strdup(const char *src)
{
	int		i;
	int		len;
	char	*str;

	if (src == NULL)
		src = "";
	len = ft_strlen(src);
	str = (char*)malloc(sizeof(*src) * (len + 1));
	if (str == ((void*)0))
		return (str);
	i = -1;
	while (src[++i] != '\0')
		str[i] = src[i];
	str[i] = '\0';
	return (str);
}
