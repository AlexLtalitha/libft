#include "libft.h"

char	*ft_strncat(char *s1, char *s2, size_t nb)
{
	size_t i;
	size_t j;

	i = 0;
	j = ft_strlen(s1);
	while (s2[i] != '\0' && i < nb)
	{
		s1[j] = s2[i];
		i++;
		j++;
	}
	s1[j] = '\0';
	return (s1);
}
